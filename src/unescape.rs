use core::fmt;

enum Replacement<'a> {
    Str(&'a str),
    Char(char),
}

impl<'a> From<&'a str> for Replacement<'a> {
    fn from(s: &'a str) -> Self {
        Replacement::Str(s)
    }
}

impl From<char> for Replacement<'_> {
    fn from(c: char) -> Self {
        Replacement::Char(c)
    }
}

/// Unescapes a HTML string and returns the unescaped string.
pub fn unescape(input: &str) -> String {
    let mut output = String::with_capacity(input.len());
    unescape_to(&mut output, input).unwrap();
    output
}

/// Unescapes a HTML string into a writer.
pub fn unescape_to(writer: &mut impl fmt::Write, input: &str) -> fmt::Result {
    // Fast path for strings without special characters
    if input.len() < 3 || !input.bytes().any(|b| matches!(b, b'&')) {
        writer.write_str(input)?;
        return Ok(());
    }

    let mut last = 0usize;
    let mut i = 0usize;

    while i < input.len() {
        let (replacement, increment) = match try_escape(&input[i..]) {
            Some((replacement, increment)) => (replacement, increment),
            None => {
                i += 1;
                continue;
            }
        };

        if last < i {
            writer.write_str(&input[last..i])?;
        }

        match replacement {
            Replacement::Str(replacement) => writer.write_str(replacement)?,
            Replacement::Char(replacement) => writer.write_char(replacement)?,
        }

        i += increment;
        last = i;
    }

    if last < input.len() {
        writer.write_str(&input[last..])?;
    }

    Ok(())
}

fn try_escape(input: &str) -> Option<(Replacement<'_>, usize)> {
    if input.len() < 3 {
        return None;
    }

    let bytes = input.as_bytes();

    match (bytes[0], bytes[1], bytes[2]) {
        (b'&', b'#', b'x' | b'X') => {
            unescape_hexadecimal_reference(input).map(|(r, i)| (r.into(), i))
        }
        (b'&', b'#', b'0'..=b'9') => unescape_decimal_reference(input).map(|(r, i)| (r.into(), i)),
        (b'&', b'#', _) => None,
        (b'&', _, _) => unescape_named_reference(bytes).map(|(r, i)| (r.into(), i)),
        _ => None,
    }
}

