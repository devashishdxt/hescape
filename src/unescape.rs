fn unescape_decimal_reference(input: &str) -> Option<(char, usize)> {
    let bytes = input.as_bytes();

    debug_assert!(matches!(bytes[0], b'&'));
    debug_assert!(matches!(bytes[1], b'#'));

    let mut i = 2;

    while i < bytes.len() {
        if bytes[i].is_ascii_digit() {
            i += 1;
        } else {
            break;
        }
    }

    let replacement = char::from_u32(input[2..i].parse::<u32>().ok()?)?;
    let has_semicolon = bytes[i] == b';';
    let increment = if has_semicolon { i + 1 } else { i };

    Some((replacement, increment))
}

fn unescape_hexadecimal_reference(input: &str) -> Option<(char, usize)> {
    let bytes = input.as_bytes();

    debug_assert!(matches!(bytes[0], b'&'));
    debug_assert!(matches!(bytes[1], b'#'));
    debug_assert!(matches!(bytes[2], b'x' | b'X'));

    let mut i = 3;

    while i < bytes.len() {
        if bytes[i].is_ascii_hexdigit() {
            i += 1;
        } else {
            break;
        }
    }

    let replacement = char::from_u32(u32::from_str_radix(&input[3..i], 16).ok()?)?;
    let has_semicolon = bytes[i] == b';';
    let increment = if has_semicolon { i + 1 } else { i };

    Some((replacement, increment))
}

